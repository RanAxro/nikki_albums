String getKeyboardCharacter(List<LogicalKeyboardKey> keys){
  if(Platform.isWindows){
    String res = "";

    for(final LogicalKeyboardKey key in keys){
      res += " + [ ${keyboardCharacterMap[key.keyId]} ]" ?? "";
    }

    return res.isNotEmpty ? res.substring(2) : res;
  }else{
    return "";
  }
}